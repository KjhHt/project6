package pms.dto;

import pms.vo.Issues;
import pms.vo.Notice;
import pms.vo.Project;

import java.util.List;

public class ProjectListDto {
    private Project prj;
    private List<Notice> noticeList;
    private List<Issues> issuesList;
    private List<NoticeSch> noticeSchList;
    private List<IssuesSch> issuesSchList;
    private Integer iprogCount[];
    private Integer jprogCount[];

    public ProjectListDto() {
    }

    public ProjectListDto(Project prj, List<Notice> noticeList, List<Issues> issuesList, List<NoticeSch> noticeSchList, List<IssuesSch> issuesSchList, Integer[] iprogCount, Integer[] jprogCount) {
        this.prj = prj;
        this.noticeList = noticeList;
        this.issuesList = issuesList;
        this.noticeSchList = noticeSchList;
        this.issuesSchList = issuesSchList;
        this.iprogCount = iprogCount;
        this.jprogCount = jprogCount;
    }

    public List<NoticeSch> getNoticeSchList() {
        return noticeSchList;
    }

    public void setNoticeSchList(List<NoticeSch> noticeSchList) {
        this.noticeSchList = noticeSchList;
    }

    public List<IssuesSch> getIssuesSchList() {
        return issuesSchList;
    }

    public void setIssuesSchList(List<IssuesSch> issuesSchList) {
        this.issuesSchList = issuesSchList;
    }

    public Project getPrj() {
        return prj;
    }

    public void setPrj(Project prj) {
        this.prj = prj;
    }

    public List<Notice> getNoticeList() {
        return noticeList;
    }

    public void setNoticeList(List<Notice> noticeList) {
        this.noticeList = noticeList;
    }

    public List<Issues> getIssuesList() {
        return issuesList;
    }

    public void setIssuesList(List<Issues> issuesList) {
        this.issuesList = issuesList;
    }

    public Integer[] getIprogCount() {
        return iprogCount;
    }

    public void setIprogCount(Integer[] iprogCount) {
        this.iprogCount = iprogCount;
    }

    public Integer[] getJprogCount() {
        return jprogCount;
    }

    public void setJprogCount(Integer[] jprogCount) {
        this.jprogCount = jprogCount;
    }
}
